import streamlit as st
import pandas as pd

# ==========================================
# 1. НАСТРОЙКА СТРАНИЦЫ (Обязательно первым шагом)
# ==========================================
st.set_page_config(
    page_title="Интерактивный BI Дашборд",
    page_icon="📈",
    layout="wide"  # Растягивает интерфейс на весь экран
)

st.title("📊 Панель аналитики розничных продаж")
st.markdown("Пример интерактивного BI-инструмента, построенного полностью на Python.")
st.write("---")


# ==========================================
# 2. ЗАГРУЗКА И КЕШИРОВАНИЕ ДАННЫХ
# ==========================================
@st.cache_data  # Ускоряет работу: данные загружаются в память один раз
def load_data() -> pd.DataFrame:
    # Имитируем выгрузку из базы данных или Excel
    raw_data = {
        "Категория": ["Электроника", "Электроника", "Бытовая техника", "Бытовая техника", "Одежда", "Одежда",
                      "Электроника", "Одежда", "Бытовая техника"],
        "Товар": ["Смартфон", "Ноутбук", "Холодильник", "Стиральная машина", "Джинсы", "Футболка", "Наушники", "Куртка",
                  "Микроволновка"],
        "Регион": ["Москва", "Санкт-Петербург", "Москва", "Новосибирск", "Санкт-Петербург", "Москва", "Новосибирск",
                   "Москва", "Санкт-Петербург"],
        "Продажи_шт": [150, 80, 45, 60, 300, 500, 220, 110, 140],
        "Выручка_руб": [7500000, 8000000, 2250000, 2400000, 900000, 1000000, 1100000, 1650000, 700000],
        "Себестоимость_руб": [6000000, 6800000, 1800000, 1900000, 540000, 500000, 800000, 1100000, 500000]
    }
    df_sales = pd.DataFrame(raw_data)
    # Рассчитываем прибыль для аналитики
    df_sales["Прибыль_руб"] = df_sales["Выручка_руб"] - df_sales["Себестоимость_руб"]
    return df_sales


df = load_data()

# ==========================================
# 3. БОКОВАЯ ПАНЕЛЬ (ФИЛЬТРЫ)
# ==========================================
st.sidebar.header("⚙️ Фильтрация данных")

# Фильтр по Региону (Одиночный выбор)
regions = ["Все регионы"] + list(df["Регион"].unique())
selected_region = st.sidebar.selectbox("Выберите регион:", regions)

# Фильтр по Категории (Множественный выбор)
categories = list(df["Категория"].unique())
selected_categories = st.sidebar.multiselect("Выберите категории:", categories, default=categories)

# Фильтр-слайдер по минимальной выручке
max_revenue = int(df["Выручка_руб"].max())
min_revenue_filter = st.sidebar.slider("Минимальная выручка по товару (руб):", 0, max_revenue, 0, step=500000)

# ==========================================
# 4. ЛОГИКА ФИЛЬТРАЦИИ (Чистый Pandas)
# ==========================================
# Применяем фильтр региона
if selected_region != "Все регионы":
    filtered_df = df[df["Регион"] == selected_region]
else:
    filtered_df = df

# Применяем фильтр категорий
filtered_df = filtered_df[filtered_df["Категория"].isin(selected_categories)]

# Применяем фильтр по ползунку выручки
filtered_df = filtered_df[filtered_df["Выручка_руб"] >= min_revenue_filter]

# ==========================================
# 5. ГЛАВНЫЙ ЭКРАН: СЛОЙ ВИЗУАЛИЗАЦИИ
# ==========================================

# Проверка: если после фильтров данных не осталось
if filtered_df.empty:
    st.warning("⚠️ Нет данных для отображения. Измените настройки фильтров.")
else:
    # --- Блок ключевых метрик (KPI) ---
    total_revenue = filtered_df["Выручка_руб"].sum()
    total_profit = filtered_df["Прибыль_руб"].sum()
    total_pcs = filtered_df["Продажи_шт"].sum()

    # Расчет рентабельности (ROI / Margin)
    avg_margin = (total_profit / total_revenue) * 100 if total_revenue > 0 else 0

    # Создаем 4 колонки для красивых карточек метрик
    kpi1, kpi2, kpi3, kpi4 = st.columns(4)
    kpi1.metric(label="💰 Общая выручка", value=f"{total_revenue:,} ₽")
    kpi2.metric(label="📈 Чистая прибыль", value=f"{total_profit:,} ₽")
    kpi3.metric(label="📦 Продано штук", value=f"{total_pcs:,} шт")
    kpi4.metric(label="🎯 Рентабельность", value=f"{avg_margin:.1f} %")

    st.write("---")

    # --- Блок графиков и таблиц ---
    # Разделяем экран на две большие колонки (график | таблица)
    col_left, col_right = st.columns([3, 2])

    with col_left:
        st.subheader("📊 Выручка и прибыль по товарам")
        # Готовим данные для группировки
        chart_data = filtered_df.groupby("Товар")[["Выручка_руб", "Прибыль_руб"]].sum()
        # Встроенный столбчатый график Streamlit
        st.bar_chart(chart_data)

    with col_right:
        st.subheader("📋 Текущий срез данных")
        # Выводим отфильтрованную таблицу (скрываем техническую себестоимость для красоты)
        display_cols = ["Категория", "Товар", "Регион", "Продажи_шт", "Выручка_руб", "Прибыль_руб"]
        st.dataframe(filtered_df[display_cols], use_container_width=True, hide_index=True)

    # --- Дополнительный интерактивный блок ---
    st.write("---")
    st.subheader("🔍 Инструмент детального анализа")

    # Позволяет пользователю выбрать конкретный товар из отфильтрованных и увидеть по нему текст-резюме
    selected_product = st.selectbox("Выберите товар для быстрой справки:", filtered_df["Товар"].unique())
    prod_info = filtered_df[filtered_df["Товар"] == selected_product].iloc[0]

    st.info(
        f"Товар **{selected_product}** (Категория: {prod_info['Категория']}) в регионе **{prod_info['Регион']}** "
        f"был продан в количестве **{prod_info['Продажи_шт']} шт.**, что принесло **{prod_info['Прибыль_руб']:,} ₽** чистой прибыли."
    )